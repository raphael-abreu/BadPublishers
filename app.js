function predator_search(list,string){

	for (var i = list.length - 1; i >= 0; i--) {
		var similarity = stringSimilarity.compareTwoStrings(string, list[i].predator); 
		if (similarity > 0.8 ){
			return list[i].predator;
		}
		
	}
	return false;
}

